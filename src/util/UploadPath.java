package util;

public class UploadPath {

	//실제 저장소 경로
	String imagePath;
	String thumbImagePath;
	String editorImagePath;
	
	//이미지 호출 경로
	String imageCallPath;
	String thumbImageCallPath;
	String editorImageCallPath;
	
	public String getImagePath() {
		return imagePath;
	}
	public void setImagePath(String imagePath) {
		this.imagePath = imagePath;
	}
	public String getThumbImagePath() {
		return thumbImagePath;
	}
	public void setThumbImagePath(String thumbImagePath) {
		this.thumbImagePath = thumbImagePath;
	}
	public String getImageCallPath() {
		return imageCallPath;
	}
	public void setImageCallPath(String imageCallPath) {
		this.imageCallPath = imageCallPath;
	}
	public String getThumbImageCallPath() {
		return thumbImageCallPath;
	}
	public void setThumbImageCallPath(String thumbImageCallPath) {
		this.thumbImageCallPath = thumbImageCallPath;
	}
	public String getEditorImagePath() {
		return editorImagePath;
	}
	public void setEditorImagePath(String editorImagePath) {
		this.editorImagePath = editorImagePath;
	}
	public String getEditorImageCallPath() {
		return editorImageCallPath;
	}
	public void setEditorImageCallPath(String editorImageCallPath) {
		this.editorImageCallPath = editorImageCallPath;
	}
	
	
}
