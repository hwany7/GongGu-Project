package util;

public class AppCancelReason {
	
	// 설문조사 
	private String cancel_price;
	private String cancel_function;
	private String cancel_product;
	private String cancel_site;
	private String cancel_mind;
	private String cancel_content;
	
	public String getCancel_price() {
		return cancel_price;
	}
	public void setCancel_price(String cancel_price) {
		this.cancel_price = cancel_price;
	}
	public String getCancel_function() {
		return cancel_function;
	}
	public void setCancel_function(String cancel_function) {
		this.cancel_function = cancel_function;
	}
	public String getCancel_product() {
		return cancel_product;
	}
	public void setCancel_product(String cancel_product) {
		this.cancel_product = cancel_product;
	}
	public String getCancel_site() {
		return cancel_site;
	}
	public void setCancel_site(String cancel_site) {
		this.cancel_site = cancel_site;
	}
	public String getCancel_mind() {
		return cancel_mind;
	}
	public void setCancel_mind(String cancel_mind) {
		this.cancel_mind = cancel_mind;
	}
	public String getCancel_content() {
		return cancel_content;
	}
	public void setCancel_content(String cancel_content) {
		this.cancel_content = cancel_content;
	} 
}
